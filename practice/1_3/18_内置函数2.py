# zip() 拉链函数
lst1 = ['胡辣汤', '疙瘩汤', '紫菜蛋花汤']
lst2 = ['我不喜欢', '我非常喜欢', '我喜欢']
d = dict(zip(lst1, lst2))
print(d) # {'胡辣汤': '我不喜欢', '疙瘩汤': '我非常喜欢', '紫菜蛋花汤': '我喜欢'}






